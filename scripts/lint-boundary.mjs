#!/usr/bin/env node
/**
 * Module-boundary lint — codifies the architectural decision that
 * `src/server/` and `src/mcp/` are isolated. Neither imports from the
 * other; both consume `src/lib/`. (Decision a5a4cde6.)
 *
 *   npm run lint:boundary                      # check (CI gate)
 *   npm run lint:boundary -- --update-baseline # rewrite the known-violations baseline
 *
 * Why a Node script instead of biome's `noRestrictedImports`?
 * Biome 2.4 supports per-import restriction rules but doesn't support a
 * baseline of grandfathered violations. We have 18 known cross-module
 * imports remaining post-#228; ship the lint as a ratchet so new cross
 * imports fail while we work down the existing list under v6.3 cards.
 *
 * Per-line escape: `// boundary-lint-allow — <reason>` on the same line.
 */

import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(SCRIPT_DIR, "..");
const SRC_DIR = resolve(REPO_ROOT, "src");
const BASELINE_PATH = resolve(SCRIPT_DIR, "boundary-lint-baseline.json");

/** Boundary rules — list of (sourceDir, forbiddenPattern) pairs. */
const RULES = [
	{
		id: "mcp-imports-server",
		sourceDir: "src/mcp/",
		// `from "@/server/..."` or `from "../server/..."` (any depth).
		pattern: /from\s+["'](?:@\/server\/|(?:\.\.\/)+server\/)/g,
		reason: "src/mcp/ must not import from src/server/. Move shared logic into src/lib/services/ (decision a5a4cde6).",
	},
	{
		id: "server-imports-mcp",
		sourceDir: "src/server/",
		pattern: /from\s+["'](?:@\/mcp\/|(?:\.\.\/)+mcp\/)/g,
		reason: "src/server/ must not import from src/mcp/. Move shared logic into src/lib/services/ (decision a5a4cde6).",
	},
];

function collectFiles(dir) {
	const out = [];
	for (const name of readdirSync(dir)) {
		const full = join(dir, name);
		const st = statSync(full);
		if (st.isDirectory()) {
			if (name === "node_modules" || name === ".next" || name === "dist") continue;
			out.push(...collectFiles(full));
			continue;
		}
		if (name.endsWith(".ts") || name.endsWith(".tsx")) out.push(full);
	}
	return out;
}

function collectViolations(file) {
	const rel = relative(REPO_ROOT, file);
	const relPosix = rel.split("\\").join("/");
	const text = readFileSync(file, "utf8");
	const lines = text.split("\n");
	const violations = [];

	for (const rule of RULES) {
		if (!relPosix.startsWith(rule.sourceDir)) continue;
		for (let i = 0; i < lines.length; i++) {
			const line = lines[i];
			if (line.includes("boundary-lint-allow")) continue;
			rule.pattern.lastIndex = 0;
			let match;
			while ((match = rule.pattern.exec(line)) !== null) {
				violations.push({
					file: relPosix,
					line: i + 1,
					col: match.index + 1,
					match: line.trim(),
					rule: rule.id,
					reason: rule.reason,
				});
			}
		}
	}

	return violations;
}

function loadBaseline() {
	try {
		const raw = readFileSync(BASELINE_PATH, "utf8");
		return JSON.parse(raw);
	} catch {
		return { generatedAt: null, violations: [] };
	}
}

function main() {
	const args = process.argv.slice(2);
	const updateBaseline = args.includes("--update-baseline");

	const files = collectFiles(SRC_DIR);
	const all = [];
	for (const f of files) all.push(...collectViolations(f));

	if (updateBaseline) {
		const baseline = {
			generatedAt: new Date().toISOString(),
			note: "Auto-generated by `npm run lint:boundary -- --update-baseline`. Existing src/mcp ↔ src/server cross-imports are grandfathered until each refactor card lands. New cross-imports fail the lint.",
			violations: all
				.map((v) => ({ file: v.file, rule: v.rule, match: v.match }))
				.sort((a, b) => a.file.localeCompare(b.file) || a.rule.localeCompare(b.rule)),
		};
		writeFileSync(BASELINE_PATH, `${JSON.stringify(baseline, null, "\t")}\n`);
		console.log(`✓ wrote ${relative(REPO_ROOT, BASELINE_PATH)} (${all.length} entries)`);
		return;
	}

	const baseline = loadBaseline();
	const baselineCounts = new Map();
	for (const v of baseline.violations) {
		const k = `${v.file}::${v.rule}::${v.match}`;
		baselineCounts.set(k, (baselineCounts.get(k) ?? 0) + 1);
	}

	const newViolations = [];
	const seenCounts = new Map();
	for (const v of all) {
		const k = `${v.file}::${v.rule}::${v.match}`;
		const allowed = baselineCounts.get(k) ?? 0;
		const seen = seenCounts.get(k) ?? 0;
		if (seen < allowed) {
			seenCounts.set(k, seen + 1);
			continue;
		}
		newViolations.push(v);
	}

	if (newViolations.length === 0) {
		const baselineSize = baseline.violations.length;
		console.log(
			`✓ boundary lint clean (${all.length} matches, ${baselineSize} grandfathered in baseline)`
		);
		return;
	}

	console.error(`✗ boundary lint found ${newViolations.length} new violation(s):`);
	for (const v of newViolations) {
		console.error(`  ${v.file}:${v.line}:${v.col}  [${v.rule}]`);
		console.error(`    ${v.match}`);
		console.error(`    → ${v.reason}`);
	}
	console.error("");
	console.error("Move the shared logic into src/lib/services/ (mirror src/lib/services/handoff.ts).");
	console.error("If this is genuinely a one-off, add `// boundary-lint-allow — <reason>` on the line.");
	process.exit(1);
}

main();
